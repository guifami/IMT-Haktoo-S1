    public void insertSignupDetails(){
        String name = txt_username.getText();
        String pwd = new String (txt_password.getPassword());
        String email = txt_email.getText();
        String ra = txt_ra.getText();
        boolean admin = false;
        txt_username.isMaximumSizeSet();
        
        if (((name.toLowerCase()).matches("admin(.*)")) && ((pwd.toLowerCase()).matches("admin(.*)"))){
        admin = true;
        }
        
        System.out.println(admin);
        
        try{
            Connection con = DBConnection.getConnection();
            String sql = "INSERT INTO USERS(NAME,PASSWORD,EMAIL,RA,ADMIN) VALUES(?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql); 
            
            pst.setString(1,name);
            pst.setString(2, pwd);
            pst.setString(3, email);
            pst.setString(4, ra);
            pst.setBoolean(5, admin);
            
            int updateRowCount = pst.executeUpdate();
            
            if(updateRowCount > 0){
                LoginPage login = new LoginPage();
                login.setVisible(true);
                dispose();
            }else{
                CustomConfirmBox ccb = new CustomConfirmBox("Ocorreu um problema ao cadastrar o usuário", this);
                ccb.setVisible(true);
            }
            
            }catch (Exception e){
                e.printStackTrace();
        }
    }
    
