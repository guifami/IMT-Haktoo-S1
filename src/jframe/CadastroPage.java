    public boolean validateSignup(){
        String name = txt_username.getText();
        String pwd = new String (txt_password.getPassword());
        String email = txt_email.getText();
        String ra = txt_ra.getText();
        
        if(name.equals("")){
            CustomDialogBox ccb = new CustomDialogBox("Campo obrigatório: Crie um nome de usuário.");
            ccb.setVisible(true);
            return false;
        }
        if(pwd.equals("")){
            CustomDialogBox ccb = new CustomDialogBox("Campo obrigatório: Crie uma senha.");
            ccb.setVisible(true);
            return false;
        }
        if(email.equals("") || !email.matches("^.+@.+\\..+$")){
            CustomDialogBox ccb = new CustomDialogBox("Campo obrigatório: Insira um E-mail válido.");
            ccb.setVisible(true);
            return false;
        }
        if(ra.equals("")){
            CustomDialogBox ccb = new CustomDialogBox("Campo obrigatório: Insira seu RA.");
            ccb.setVisible(true);
            return false;
        }
        return true;
    }
    
